"""Funciones auxiliares para transformar texto."""

def to_upper_case(texto):
    """Convierte el texto a mayusculas."""
    return texto.upper()

def to_lower_case(texto):
    """Convierte el texto a minusculas."""
    return texto.lower()

def to_capitalize(texto):
    """Convierte el texto a capitalizado."""
    return texto.capitalize()
